package com.nikita.data.object;



public class RestPosition {
	private String f_name;
	private String f_article;
	private int f_quant;
	
	public String getF_name() {
		return f_name;
	}
	public void setF_name(String f_name) {
		this.f_name = f_name;
	}
	public String getF_article() {
		return f_article;
	}
	public void setF_article(String f_article) {
		this.f_article = f_article;
	}
	public int getF_quant() {
		return f_quant;
	}
	public void setF_quant(int f_quant) {
		this.f_quant = f_quant;
	}

}
